//
// @module models
//----------------------------------------------------------------------------------------------------------------------

import config from '../config'
import setupThinky from  'thinky'

var thinky = setupThinky(config.rethinkdb);
var db = { r: thinky.r, errors: thinky.Errors };

//----------------------------------------------------------------------------------------------------------------------

db.User = thinky.createModel('users', {
    name: String,
    email: String,
    created: { _type: Date, default: Date.now() }
}, { pk: 'email' });

//----------------------------------------------------------------------------------------------------------------------

export default db;

